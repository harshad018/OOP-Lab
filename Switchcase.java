
import java.util.*;

class Switchcase {



    public static void main (String args[]){


	
	Scanner scan = new Scanner (System.in);


	System.out.println("Enter the value of i: ");

	int i = scan.nextInt();


	switch (i){

	case 1:
	    System.out.println("Entered value is 1");
	    break;


        case 2:
	    System.out.println("Entered value is 2");
	    break;

	case 3:
	    System.out.println("Entered value is 3");
	    break;

	case 4:
	    System.out.println("Entered value is 4");
	    break;

	default:
	    System.out.println("Entered value is invalid. Please try again later.");

	













	}












    }




















}
